						if ((lastslash = strrchr(swathfile, '/')) != NULL)
							strcat(messagestr,&(lastslash[1]));
						else
							strcat(messagestr,swathfile);
						do_mbview_message_on(messagestr, instance);
fprintf(stderr,"%s\n",messagestr);

