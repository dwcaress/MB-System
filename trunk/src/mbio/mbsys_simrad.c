int mbsys_simrad_ttimes(verbose,mbio_ptr,store_ptr,kind,nbeams,
	ttimes,angles,angles_forward,angles_null,
	depth_offset,alongtrack_offset,flags,ssv,error)
int	verbose;
char	*mbio_ptr;
char	*store_ptr;
int	*kind;
int	*nbeams;
double	*ttimes;
double	*angles;
double	*angles_forward;
double	*angles_null;
double	*depth_offset;
double	*alongtrack_offset;
int	*flags;
double	*ssv;
int	*error;
{
	char	*function_name = "mbsys_simrad_ttimes";
	int	status = MB_SUCCESS;
	struct mb_io_struct *mb_io_ptr;
	struct mbsys_simrad_struct *store;
	struct mbsys_simrad_survey_struct *ping;
	double	ttscale;
	double	depthadd;
	double	*angles_simrad;
	int	i, j;

	/* print input debug statements */
	if (verbose >= 2)
		{
		fprintf(stderr,"\ndbg2  MBIO function <%s> called\n",
			function_name);
		fprintf(stderr,"dbg2  Input arguments:\n");
		fprintf(stderr,"dbg2       verbose:    %d\n",verbose);
		fprintf(stderr,"dbg2       mb_ptr:     %d\n",mbio_ptr);
		fprintf(stderr,"dbg2       store_ptr:  %d\n",store_ptr);
		fprintf(stderr,"dbg2       ttimes:     %d\n",ttimes);
		fprintf(stderr,"dbg2       angles_xtrk:%d\n",angles);
		fprintf(stderr,"dbg2       angles_ltrk:%d\n",angles_forward);
		fprintf(stderr,"dbg2       angles_null:%d\n",angles_null);
		fprintf(stderr,"dbg2       depth_off:  %d\n",depth_offset);
		fprintf(stderr,"dbg2       ltrk_off:   %d\n",alongtrack_offset);
		fprintf(stderr,"dbg2       flags:      %d\n",flags);
		}

	/* get mbio descriptor */
	mb_io_ptr = (struct mb_io_struct *) mbio_ptr;

	/* get data structure pointer */
	store = (struct mbsys_simrad_struct *) store_ptr;

	/* get data kind */
	*kind = store->kind;

	/* extract data from structure */
	if (*kind == MB_DATA_DATA)
		{
		/* get survey data structure */
		ping = (struct mbsys_simrad_survey_struct *) store->ping;

		/* get nbeams */
		*nbeams = mb_io_ptr->beams_bath;

		/* get depth offset (heave + heave offset) */
		if (store->sonar == MBSYS_SIMRAD_EM12S)
			depthadd = 0.01*ping->ping_heave + store->em12_td;
		else if (store->sonar == MBSYS_SIMRAD_EM12D)
			depthadd = 0.01*ping->ping_heave + store->em12_td;
		else if (store->sonar == MBSYS_SIMRAD_EM100)
			depthadd = 0.01*ping->ping_heave + store->em100_td;
		else if (store->sonar == MBSYS_SIMRAD_EM1000)
			depthadd = 0.01*ping->ping_heave + store->em1000_td;
		*ssv = 0.1 * ping->sound_vel;

		/* get travel times, angles, and flags */
		if (store->sonar == MBSYS_SIMRAD_EM1000)
			{
			if (ping->bath_mode == 1)
			    angles_simrad = angles_EM1000_70_2_MS_48_FAIS;
			else if (ping->bath_mode == 2)
			    angles_simrad = angles_EM1000_70_2_MS_48_FAIS;
			else if (ping->bath_mode == 3)
			    angles_simrad = angles_EM1000_SHALLOW;
			}
		else if (store->sonar == MBSYS_SIMRAD_EM12S)
			{
			if (ping->bath_mode == 1)
			    angles_simrad = angles_EM12S_ISO_ANG_SHALLOW;
			else if (ping->bath_mode == 2)
			    angles_simrad = angles_EM12S_ISO_ANG_DEEP;
			else if (ping->bath_mode == 3)
			    angles_simrad = angles_EM12S_SHALLOW;
			else if (ping->bath_mode == 4)
			    angles_simrad = angles_EM12S_120;
			else if (ping->bath_mode == 5)
			    angles_simrad = angles_EM12S_105;
			else if (ping->bath_mode == 6)
			    angles_simrad = angles_EM12S_90;			
			}
		else if (store->sonar == MBSYS_SIMRAD_EM12D)
			{
			if (ping->bath_mode == 1)
			    angles_simrad = angles_EM12D_ISO_ANG_SHALLOW;
			else if (ping->bath_mode == 2)
			    angles_simrad = angles_EM12D_ISO_ANG_DEEP;
			else if (ping->bath_mode == 3)
			    angles_simrad = angles_EM12D_SHALLOW;
			else if (ping->bath_mode == 4)
			    angles_simrad = angles_EM12D_150;
			else if (ping->bath_mode == 5)
			    angles_simrad = angles_EM12D_140;
			else if (ping->bath_mode == 6)
			    angles_simrad = angles_EM12D_128;			
			else if (ping->bath_mode == 7)
			    angles_simrad = angles_EM12D_114;			
			else if (ping->bath_mode == 8)
			    angles_simrad = angles_EM12D_98;			
			}
		if (store->sonar == MBSYS_SIMRAD_EM1000)
			{
			ttscale = 0.00005;
			}
		else if (store->sonar == MBSYS_SIMRAD_EM12S 
			&& ping->bath_res == 1)
			{
			ttscale    = 0.0002;
			}
		else if (store->sonar == MBSYS_SIMRAD_EM12S 
			&& ping->bath_res == 2)
			ttscale    = 0.0008;
		else if (store->sonar == MBSYS_SIMRAD_EM121)
			ttscale    = 0.0001 * ping->range_res;
		else
			ttscale    = 0.0002;
		for (i=0;i<mb_io_ptr->beams_bath;i++)
			{
			ttimes[i] = ttscale * ping->tt[i];
			angles[i] = -angles_simrad[i];
			angles_forward[i] = 0.0;
			if (store->sonar == MBSYS_SIMRAD_EM1000)
			    angles_null[i] = fabs(angles[i]);
			else if (store->sonar == MBSYS_SIMRAD_EM12S)
			    angles_null[i] = 0.0;
			else if (store->sonar == MBSYS_SIMRAD_EM12D)
			    angles_null[i] = 0.0; /* wrong for sure */
			depth_offset[i] = depthadd;
			alongtrack_offset[i] = 0.0;
			if (ping->bath[i] < 0)
				flags[i] = MB_YES;
			else
				flags[i] = MB_NO;
			}

		/* set status */
		*error = MB_ERROR_NO_ERROR;
		status = MB_SUCCESS;

		/* done translating values */

		}

	/* deal with comment */
	else if (*kind == MB_DATA_COMMENT)
		{
		/* set status */
		*error = MB_ERROR_COMMENT;
		status = MB_FAILURE;
		}

	/* deal with other record type */
	else
		{
		/* set status */
		*error = MB_ERROR_OTHER;
		status = MB_FAILURE;
		}

	/* print output debug statements */
	if (verbose >= 2)
		{
		fprintf(stderr,"\ndbg2  MBIO function <%s> completed\n",
			function_name);
		fprintf(stderr,"dbg2  Return values:\n");
		fprintf(stderr,"dbg2       kind:       %d\n",*kind);
		}
	if (verbose >= 2 && *error == MB_ERROR_NO_ERROR)
		{
		fprintf(stderr,"dbg2       ssv:        %f\n",*ssv);
		fprintf(stderr,"dbg2       nbeams:     %d\n",*nbeams);
		for (i=0;i<*nbeams;i++)
			fprintf(stderr,"dbg2       beam %d: tt:%f  angle_xtrk:%f  angle_ltrk:%f  angle_null:%f  depth_off:%f  ltrk_off:%f  flag:%d\n",
				i,ttimes[i],angles[i],
				angles_forward[i],angles_null[i],
				depth_offset[i],alongtrack_offset[i],
				flags[i]);
		}
	if (verbose >= 2)
		{
		fprintf(stderr,"dbg2       error:      %d\n",*error);
		fprintf(stderr,"dbg2  Return status:\n");
		fprintf(stderr,"dbg2       status:     %d\n",status);
		}

	/* return status */
	return(status);
}
